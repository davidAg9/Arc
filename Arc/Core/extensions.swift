    }
    public static var arcForeground:UIColor {
        return  UIColor(named:"arcforeground")!
    }
    public static var arcPrimary:UIColor {
        return  UIColor(named:"arcprimary")!
    }
    public static var arcsecondary:UIColor {
        return  UIColor(named:"arcsecondary")!
    }
}



